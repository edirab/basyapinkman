<br />
И кто же та дева, что стала тому виной?<br />
Совсем из других краёв, а той же бедой больна:<br />
Она одного подождёт, эфенди - её...<br />
Вот уже тысячу лет ни покоя, ни сна.<br />
<br />
И был бы кто на примете, кто на язык мастак:<br />
Он прямо бы в уши этим двоим сказал:<br />
"Вряд ли ребята протянете вы до ста,<br />
Если поверите чести, а не сердцам".<br />
<br />
Ночь, соловей и стандартный розовый куст,<br />
(Можете заменить на канаву отхожих мест),<br />
Вот бы хоть тут сорвались слова бы с уст<br />
С верой, что кто-то не выдаст, а кто-то не съест.<br />
<br />
Думала, будет он - благородный дон:<br />
Здраво, вроде бы, судит, не рубит с плеч,<br />
Ну так и с ним по всему не построишь дом,<br />
Сына не вырастишь, коли в рассудке течь.<br />
<br />
Знать, для тебя, эфенди, я - та же дыра,<br />
Я и сама в такую сейчас гляжу...<br />
